export const yearDayToMonthDays = (() => {
    const result: Record<number, {month: number; day: number}> = {};

    for (const month in monthDaysToYearDay) {
        for (const day in monthDaysToYearDay[month]) {
            const thing = monthDaysToYearDay[month][day];
            result[thing] = {
                month: parseInt(month),
                day: parseInt(day)
            };
        }
    }

    return result;
})();
